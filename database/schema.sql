create or replace function public.calcular_resumo_semestral(p_matricula_turma_id uuid)
returns table (
  subtotal_percentual numeric(6,2),
  penalidade_percentual numeric(6,2),
  total_percentual numeric(6,2),
  equivalente_nota_10 numeric(5,2)
)
language sql
stable
security definer
set search_path = public
as $$
  with latest_items as (
    select distinct on (ia.criterio_id)
      ia.criterio_id,
      ia.nota_ponderada_percentual
    from public.itens_avaliados ia
    join public.avaliacoes a on a.id = ia.avaliacao_id
    where a.matricula_turma_id = p_matricula_turma_id
      and a.status = 'publicado'
    order by ia.criterio_id, a.avaliado_em desc, a.created_at desc, ia.created_at desc
  ),
  absence_penalty as (
    select coalesce(sum(horas), 0)::numeric(6,2) as penalidade_percentual
    from public.ausencias
    where matricula_turma_id = p_matricula_turma_id
      and justificada = false
  ),
  subtotal as (
    select coalesce(sum(nota_ponderada_percentual), 0)::numeric(6,2) as subtotal_percentual
    from latest_items
  )
  select
    s.subtotal_percentual,
    a.penalidade_percentual,
    greatest(s.subtotal_percentual - a.penalidade_percentual, 0)::numeric(6,2) as total_percentual,
    round(greatest(s.subtotal_percentual - a.penalidade_percentual, 0) / 10.0, 2)::numeric(5,2) as equivalente_nota_10
  from subtotal s
  cross join absence_penalty a;
$$;

create or replace function public.criar_avaliacao_com_itens(
  p_matricula_turma_id uuid,
  p_tipo_lancamento text,
  p_referencia text,
  p_observacoes text,
  p_status text,
  p_itens jsonb,
  p_avaliado_em timestamptz default now()
)
returns uuid
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  v_avaliacao_id uuid;
  v_semestre_id uuid;
  v_item jsonb;
  v_criterio_id uuid;
  v_nota_bruta numeric(5,2);
  v_feedback text;
  v_peso_percentual numeric(5,2);
begin
  if private.current_profile_code() <> 'professor' then
    raise exception 'Apenas professores podem criar avaliações por esta função.';
  end if;

  if not private.can_manage_grades(p_matricula_turma_id) then
    raise exception 'Professor sem vínculo ativo com a matrícula informada.';
  end if;

  if p_tipo_lancamento not in ('parcial', 'revisao', 'fechamento') then
    raise exception 'Tipo de lançamento inválido.';
  end if;

  if p_status not in ('rascunho', 'publicado') then
    raise exception 'Status de lançamento inválido.';
  end if;

  if trim(coalesce(p_referencia, '')) = '' then
    raise exception 'A referência do lançamento é obrigatória.';
  end if;

  if p_itens is null
    or jsonb_typeof(p_itens) <> 'array'
    or jsonb_array_length(p_itens) = 0 then
    raise exception 'É necessário informar ao menos um critério avaliado.';
  end if;

  select t.semestre_id
  into v_semestre_id
  from public.matriculas_turma mt
  join public.turmas t on t.id = mt.turma_id
  where mt.id = p_matricula_turma_id
  limit 1;

  if v_semestre_id is null then
    raise exception 'Não foi possível identificar o semestre da matrícula.';
  end if;

  insert into public.avaliacoes (
    matricula_turma_id,
    professor_id,
    semestre_id,
    tipo_lancamento,
    referencia,
    observacoes,
    status,
    avaliado_em
  )
  values (
    p_matricula_turma_id,
    auth.uid(),
    v_semestre_id,
    p_tipo_lancamento,
    trim(p_referencia),
    nullif(trim(coalesce(p_observacoes, '')), ''),
    p_status,
    coalesce(p_avaliado_em, now())
  )
  returning id into v_avaliacao_id;

  for v_item in
    select value
    from jsonb_array_elements(p_itens)
  loop
    v_criterio_id := nullif(v_item ->> 'criterio_id', '')::uuid;
    v_nota_bruta := nullif(v_item ->> 'nota_bruta', '')::numeric;
    v_feedback := nullif(trim(coalesce(v_item ->> 'feedback', '')), '');

    if v_criterio_id is null or v_nota_bruta is null then
      raise exception 'Item avaliado inválido.';
    end if;

    select c.peso_percentual
    into v_peso_percentual
    from public.criterios_avaliacao c
    where c.id = v_criterio_id
      and c.ativo = true
    limit 1;

    if v_peso_percentual is null then
      raise exception 'Critério de avaliação não encontrado ou inativo.';
    end if;

    insert into public.itens_avaliados (
      avaliacao_id,
      criterio_id,
      nota_bruta,
      peso_aplicado_percentual,
      feedback
    )
    values (
      v_avaliacao_id,
      v_criterio_id,
      v_nota_bruta,
      v_peso_percentual,
      v_feedback
    );
  end loop;

  return v_avaliacao_id;
end;
$$;

