create or replace function public.vincular_liberacao_excepcional_auditoria(
  p_liberacao_excepcional_id uuid,
  p_tabela text,
  p_registro_ids uuid[]
)
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  updated_count integer := 0;
begin
  if auth.uid() is null then
    raise exception 'Usuário autenticado não encontrado para vincular a auditoria.';
  end if;

  if p_liberacao_excepcional_id is null then
    return 0;
  end if;

  if p_registro_ids is null or array_length(p_registro_ids, 1) is null then
    return 0;
  end if;

  if not exists (
    select 1
    from public.liberacoes_excepcionais le
    where le.id = p_liberacao_excepcional_id
      and le.usuario_autorizado_id = auth.uid()
  ) then
    raise exception 'A liberação excepcional informada não pertence ao usuário autenticado.';
  end if;

  update public.historico_alteracoes h
  set liberacao_excepcional_id = p_liberacao_excepcional_id
  where h.tabela = p_tabela
    and h.registro_id = any(p_registro_ids)
    and (h.usuario_id = auth.uid() or h.usuario_id is null)
    and h.liberacao_excepcional_id is null
    and h.created_at >= now() - interval '15 minutes';

  get diagnostics updated_count = row_count;

  return updated_count;
end;
$$;

create or replace function private.resolve_audit_unit_id(
  p_table_name text,
  p_record_data jsonb,
  p_actor_unit_id uuid default null
)
returns uuid
language plpgsql
stable
security definer
set search_path = public
as $$
declare
  resolved_unit_id uuid;
begin
  if p_record_data is null then
    return p_actor_unit_id;
  end if;

  begin
    resolved_unit_id :=
      nullif(trim(coalesce(p_record_data ->> 'unidade_id', '')), '')::uuid;
  exception
    when invalid_text_representation then
      resolved_unit_id := null;
  end;

  if resolved_unit_id is not null then
    return resolved_unit_id;
  end if;

  case p_table_name
    when 'usuarios' then
      select u.unidade_id
      into resolved_unit_id
      from public.usuarios u
      where u.id = nullif(p_record_data ->> 'id', '')::uuid
      limit 1;

    when 'alunos' then
      select coalesce(a.unidade_id, u.unidade_id)
      into resolved_unit_id
      from public.alunos a
      left join public.usuarios u on u.id = a.usuario_id
      where a.usuario_id = nullif(p_record_data ->> 'usuario_id', '')::uuid
      limit 1;

    when 'professores' then
      select coalesce(p.unidade_id, u.unidade_id)
      into resolved_unit_id
      from public.professores p
      left join public.usuarios u on u.id = p.usuario_id
      where p.usuario_id = nullif(p_record_data ->> 'usuario_id', '')::uuid
      limit 1;

    when 'coordenadores' then
      select coalesce(c.unidade_id, u.unidade_id)
      into resolved_unit_id
      from public.coordenadores c
      left join public.usuarios u on u.id = c.usuario_id
      where c.usuario_id = nullif(p_record_data ->> 'usuario_id', '')::uuid
      limit 1;

    when 'semestres' then
      select s.unidade_id
      into resolved_unit_id
      from public.semestres s
      where s.id = coalesce(
        nullif(p_record_data ->> 'id', '')::uuid,
        nullif(p_record_data ->> 'semestre_id', '')::uuid
      )
      limit 1;

    when 'turmas' then
      select s.unidade_id
      into resolved_unit_id
      from public.turmas t
      join public.semestres s on s.id = t.semestre_id
      where t.id = coalesce(
        nullif(p_record_data ->> 'id', '')::uuid,
        nullif(p_record_data ->> 'turma_id', '')::uuid
      )
      limit 1;

    when 'matriculas_turma', 'ausencias', 'vinculos_professor_aluno' then
      select s.unidade_id
      into resolved_unit_id
      from public.matriculas_turma m
      join public.turmas t on t.id = m.turma_id
      join public.semestres s on s.id = t.semestre_id
      where m.id = coalesce(
        nullif(p_record_data ->> 'id', '')::uuid,
        nullif(p_record_data ->> 'matricula_turma_id', '')::uuid
      )
      limit 1;

    when 'professor_areas_estagio' then
      select coalesce(p.unidade_id, u.unidade_id)
      into resolved_unit_id
      from public.professores p
      left join public.usuarios u on u.id = p.usuario_id
      where p.usuario_id = nullif(p_record_data ->> 'professor_id', '')::uuid
      limit 1;

    when 'avaliacoes' then
      select s.unidade_id
      into resolved_unit_id
      from public.semestres s
      where s.id = nullif(p_record_data ->> 'semestre_id', '')::uuid
      limit 1;

      if resolved_unit_id is null then
        select s.unidade_id
        into resolved_unit_id
        from public.matriculas_turma m
        join public.turmas t on t.id = m.turma_id
        join public.semestres s on s.id = t.semestre_id
        where m.id = nullif(p_record_data ->> 'matricula_turma_id', '')::uuid
        limit 1;
      end if;

    when 'itens_avaliados' then
      select s.unidade_id
      into resolved_unit_id
      from public.avaliacoes a
      join public.semestres s on s.id = a.semestre_id
      where a.id = nullif(p_record_data ->> 'avaliacao_id', '')::uuid
      limit 1;

    when 'pacientes_clinica' then
      select p.unidade_id
      into resolved_unit_id
      from public.pacientes_clinica p
      where p.id = coalesce(
        nullif(p_record_data ->> 'id', '')::uuid,
        nullif(p_record_data ->> 'paciente_id', '')::uuid
      )
      limit 1;

    when 'casos_clinicos' then
      select c.unidade_id
      into resolved_unit_id
      from public.casos_clinicos c
      where c.id = coalesce(
        nullif(p_record_data ->> 'id', '')::uuid,
        nullif(p_record_data ->> 'caso_clinico_id', '')::uuid
      )
      limit 1;

      if resolved_unit_id is null then
        select s.unidade_id
        into resolved_unit_id
        from public.semestres s
        where s.id = nullif(p_record_data ->> 'semestre_id', '')::uuid
        limit 1;
      end if;

    when 'casos_clinicos_horarios', 'registros_clinicos', 'notificacoes_clinicas' then
      select c.unidade_id
      into resolved_unit_id
      from public.casos_clinicos c
      where c.id = nullif(p_record_data ->> 'caso_clinico_id', '')::uuid
      limit 1;

    when 'documentos_aluno' then
      select d.unidade_id
      into resolved_unit_id
      from public.documentos_aluno d
      where d.id = coalesce(
        nullif(p_record_data ->> 'id', '')::uuid,
        nullif(p_record_data ->> 'documento_id', '')::uuid
      )
      limit 1;

    when 'notificacoes_documentos_aluno' then
      select d.unidade_id
      into resolved_unit_id
      from public.documentos_aluno d
      where d.id = nullif(p_record_data ->> 'documento_id', '')::uuid
      limit 1;
  end case;

  return coalesce(resolved_unit_id, p_actor_unit_id);
end;
$$;

create or replace function public.audit_changes()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  current_user_id uuid;
  current_user_unit_id uuid;
  record_unit_id uuid;
  current_exceptional_release_id uuid;
  record_payload jsonb;
begin
  current_user_id := auth.uid();
  current_user_unit_id := (
    select u.unidade_id
    from public.usuarios u
    where u.id = current_user_id
    limit 1
  );

  if tg_op = 'DELETE' then
    record_payload := to_jsonb(old);
  else
    record_payload := to_jsonb(new);
  end if;

  current_exceptional_release_id := private.current_exceptional_release_id();

  record_unit_id := private.resolve_audit_unit_id(
    tg_table_name,
    record_payload,
    current_user_unit_id
  );

  if tg_op = 'INSERT' then
    insert into public.historico_alteracoes (
      tabela,
      registro_id,
      acao,
      dados_depois,
      usuario_id,
      unidade_id,
      liberacao_excepcional_id
    )
    values (
      tg_table_name,
      new.id,
      tg_op,
      to_jsonb(new),
      current_user_id,
      record_unit_id,
      current_exceptional_release_id
    );
    return new;
  elsif tg_op = 'UPDATE' then
    insert into public.historico_alteracoes (
      tabela,
      registro_id,
      acao,
      dados_antes,
      dados_depois,
      usuario_id,
      unidade_id,
      liberacao_excepcional_id
    )
    values (
      tg_table_name,
      new.id,
      tg_op,
      to_jsonb(old),
      to_jsonb(new),
      current_user_id,
      record_unit_id,
      current_exceptional_release_id
    );
    return new;
  elsif tg_op = 'DELETE' then
    insert into public.historico_alteracoes (
      tabela,
      registro_id,
      acao,
      dados_antes,
      usuario_id,
      unidade_id,
      liberacao_excepcional_id
    )
    values (
      tg_table_name,
      old.id,
      tg_op,
      to_jsonb(old),
      current_user_id,
      record_unit_id,
      current_exceptional_release_id
    );
    return old;
  end if;

  return null;
end;
$$;

update public.historico_alteracoes h
set unidade_id = coalesce(
  private.resolve_audit_unit_id(
    h.tabela,
    coalesce(h.dados_depois, h.dados_antes),
    null
  ),
  (
    select u.unidade_id
    from public.usuarios u
    where u.id = h.usuario_id
    limit 1
  )
)
where h.unidade_id is null;

create or replace function public.registrar_acesso_sistema()
returns uuid
language plpgsql
set search_path = public
as $$
declare
  current_user_id uuid;
  current_user_record public.usuarios%rowtype;
  current_profile_code text;
  access_id uuid;
begin
  current_user_id := auth.uid();

  if current_user_id is null then
    raise exception 'Usuário autenticado não encontrado para registrar acesso.';
  end if;

  select u.*
  into current_user_record
  from public.usuarios u
  where u.id = current_user_id
    and u.ativo = true
  limit 1;

  if not found then
    raise exception 'Usuário ativo não encontrado para registrar acesso.';
  end if;

  select p.codigo
  into current_profile_code
  from public.perfis p
  where p.id = current_user_record.perfil_id
  limit 1;

  insert into public.acessos_sistema (
    usuario_id,
    unidade_id,
    nome_usuario,
    email,
    perfil
  )
  values (
    current_user_record.id,
    current_user_record.unidade_id,
    current_user_record.nome_completo,
    current_user_record.email,
    current_profile_code
  )
  returning id into access_id;

  return access_id;
end;
$$;

create or replace function public.sync_notificacoes_clinicas_avaliacao()
returns trigger
language plpgsql
set search_path = public
as $$
declare
  v_professor_id uuid;
  v_student_id uuid;
  v_student_name text;
  v_patient_name text;
  v_notification_type text;
  v_title text;
  v_message text;
begin
  if new.tipo not in ('avaliacao', 'plano_tratamento', 'evolucao') then
    return new;
  end if;

  if tg_op = 'UPDATE' and old.status is not distinct from new.status then
    return new;
  end if;

  select
    c.professor_id,
    mt.aluno_id,
    u.nome_completo,
    p.nome
  into
    v_professor_id,
    v_student_id,
    v_student_name,
    v_patient_name
  from public.casos_clinicos c
  join public.matriculas_turma mt on mt.id = c.matricula_turma_id
  join public.usuarios u on u.id = mt.aluno_id
  join public.pacientes_clinica p on p.id = c.paciente_id
  where c.id = new.caso_clinico_id
  limit 1;

  if v_professor_id is null or v_student_id is null then
    return new;
  end if;

  if new.status = 'enviado' and private.current_profile_code() = 'aluno' then
    if new.tipo = 'avaliacao' then
      v_notification_type := 'avaliacao_enviada_supervisao';
      v_title := 'Avaliacao enviada para supervisao';
      v_message :=
        coalesce(v_student_name, 'O aluno')
        || ' enviou a avaliacao de '
        || coalesce(v_patient_name, 'um paciente')
        || '.';
    elsif new.tipo = 'plano_tratamento' then
      v_notification_type := 'plano_tratamento_enviado_supervisao';
      v_title := 'Plano de tratamento enviado para supervisao';
      v_message :=
        coalesce(v_student_name, 'O aluno')
        || ' enviou o plano de tratamento de '
        || coalesce(v_patient_name, 'um paciente')
        || '.';
    else
      v_notification_type := 'evolucao_enviada_supervisao';
      v_title := 'Evolucao enviada para supervisao';
      v_message :=
        coalesce(v_student_name, 'O aluno')
        || ' enviou a evolucao de '
        || coalesce(v_patient_name, 'um paciente')
        || '.';
    end if;

    insert into public.notificacoes_clinicas (
      unidade_id,
      usuario_id,
      caso_clinico_id,
      registro_clinico_id,
      tipo,
      titulo,
      mensagem
    )
    values (
      new.unidade_id,
      v_professor_id,
      new.caso_clinico_id,
      new.id,
      v_notification_type,
      v_title,
      v_message
    );

    return new;
  end if;

  if
    new.status = 'ajustes_solicitados'
    and private.current_profile_code() = 'professor'
  then
    if new.tipo = 'avaliacao' then
      v_notification_type := 'avaliacao_ajustes_solicitados';
      v_title := 'Ajustes solicitados na avaliacao';
      v_message :=
        'O supervisor registrou parecer e solicitou ajustes na avaliacao de '
        || coalesce(v_patient_name, 'seu paciente')
        || '.';
    elsif new.tipo = 'plano_tratamento' then
      v_notification_type := 'plano_tratamento_ajustes_solicitados';
      v_title := 'Ajustes solicitados no plano de tratamento';
      v_message :=
        'O supervisor registrou parecer e solicitou ajustes no plano de tratamento de '
        || coalesce(v_patient_name, 'seu paciente')
        || '.';
    else
      v_notification_type := 'evolucao_ajustes_solicitados';
      v_title := 'Ajustes solicitados na evolucao';
      v_message :=
        'O supervisor registrou parecer e solicitou ajustes na evolucao de '
        || coalesce(v_patient_name, 'seu paciente')
        || '.';
    end if;

    insert into public.notificacoes_clinicas (
      unidade_id,
      usuario_id,
      caso_clinico_id,
      registro_clinico_id,
      tipo,
      titulo,
      mensagem
    )
    values (
      new.unidade_id,
      v_student_id,
      new.caso_clinico_id,
      new.id,
      v_notification_type,
      v_title,
      v_message
    );

    return new;
  end if;

  if new.status = 'aprovado' and private.current_profile_code() = 'professor' then
    if new.tipo = 'avaliacao' then
      v_notification_type := 'avaliacao_aprovada';
      v_title := 'Avaliacao aprovada';
      v_message :=
        'O supervisor aprovou a avaliacao de '
        || coalesce(v_patient_name, 'seu paciente')
        || '.';
    elsif new.tipo = 'plano_tratamento' then
      v_notification_type := 'plano_tratamento_aprovado';
      v_title := 'Plano de tratamento aprovado';
      v_message :=
        'O supervisor aprovou o plano de tratamento de '
        || coalesce(v_patient_name, 'seu paciente')
        || '.';
    else
      v_notification_type := 'evolucao_aprovada';
      v_title := 'Evolucao aprovada';
      v_message :=
        'O supervisor aprovou a evolucao de '
        || coalesce(v_patient_name, 'seu paciente')
        || '.';
    end if;

    insert into public.notificacoes_clinicas (
      unidade_id,
      usuario_id,
      caso_clinico_id,
      registro_clinico_id,
      tipo,
      titulo,
      mensagem
    )
